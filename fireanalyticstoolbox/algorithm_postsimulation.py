            if sink:
                # build line add to sink
                for i, j, time in data[-1]:
                    i_x_px, i_y_ln = id2xy(i, W, H)
                    i_x_geo, i_y_geo = transform_coords_to_georef(i_x_px + 0.5, i_y_ln + 0.5, GT)
