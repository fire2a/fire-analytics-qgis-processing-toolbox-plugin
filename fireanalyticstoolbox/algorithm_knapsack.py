        args = {}
        for r in raster_list:
            if gt := gdal.Open(r.publicSource(), gdal.GA_ReadOnly).GetGeoTransform():
                args["--geotransform"] = str(gt)
                break
        for r in raster_list:
            if authid := r.crs().authid():
                args["--authid"] = authid
